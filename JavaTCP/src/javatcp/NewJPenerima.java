    public class File extends Thread{
        @Override
        public void run() {
            while (true) {
                try {
                    String receive;
                    ServerSocket ss = new ServerSocket(2000);
                    Socket s = ss.accept();
                    BufferedReader a = new BufferedReader(new InputStreamReader(s.getInputStream()));
                    receive = a.readLine();
                    System.out.println("Received: " + receive);
                    if (receive.equalsIgnoreCase("stop")) {
                        System.exit(0);
                    } else {
                        jLabel1.setText(receive);
                    }
                    s.close();
                    ss.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        
    }
